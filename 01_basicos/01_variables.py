# 1. Variables
# Una variable es como una caja donde guardamos información.
nombre = "Paulo Gutierrez"  # Esto es una cadena de texto (string)
edad = 26             # Esto es un número entero (int)
altura = 1.83         # Esto es un número decimal (float)
es_estudiante = True  # Esto es un booleano (bool)
comida_favorita = "sopaipillas"  # Esto es una cadena de texto (string)
# 2. Imprimir en pantalla
# Usamos la función print() para mostrar información.
print("Hola, mi nombre es:", nombre)
print("Tengo", edad, "años.")
print("Mido", altura, "metros.")
print("¿Soy estudiante?", es_estudiante)
print("Mi comida favorita es:", comida_favorita)
